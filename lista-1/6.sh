#!/bin/bash

echo '$HOME' "(valor atual: /home/ifpb): variável que retorna o diretório inicial do usuário atual."

echo -e "\n"'$PWD' "(valor atual: /home/ifpb/lista-1): variável que retorna o caminho absoluto onde o usuário se encontra."

echo -e "\n"'$SHELL' "(valor atual: /bin/bash): variável que retorna o interpretador de comandos do terminal."

echo -e "\n"'$LANG' "(valor atual: pt_BR.UTF-8): variável que retorna o idioma do sistema e o tipo de codificação de caracteres."

echo -e "\n"'$USER' "(valor atual: ifpb): variável que retorna o usuário atual que está logado."

echo -e "\n"'$_' "(valor atual: clear): variável que retorna o último comando inserido."

echo -e "\n"'$TERM' "(valor atual: linux): variável que retorna o sistema operacional que está sendo emulado."

echo -e "\n"'$BASH_VERSION' "(valor atual: 5.0.3(1)-release): variável que retorna a versão do bash."
